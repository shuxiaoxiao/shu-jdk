package com.shuframework.testmodel;

import java.io.Serializable;

/**
 * 重写了equals()和hashCode() 并且实现Serializable接口 的pojo
 * 
 * @author shu
 *
 */
public class BookInfo5 implements Serializable {
	
	private static final long serialVersionUID = 5589303451418047322L;
	
	private Integer id;
	private String name;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public BookInfo5() {}
	
	public BookInfo5(Integer id, String name) {
		this.id = id;
		this.name = name;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		BookInfo5 other = (BookInfo5) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
	
	@Override
	public String toString() {
		return "BookInfo5 [id=" + id + ", name=" + name + "]";
	}
}
